    minimum = sys.maxsize

    for current in color_table:
        length = sqrt(((r - current['r']) ** 2) + ((g - current['g']) ** 2) + ((b - current['b']) ** 2))

        if length < minimum:
            minimum = length
            color = current['name']

    return color

